var add = require('../lib/add')

describe('大数相加add方法', function () {
  it('字符串"42329"加上字符串"21532"等于"63861"', function () {
    add('42329', '21532')
      .should.equal('63861')
  })
  
  it('"843529812342341234"加上"236124361425345435"等于"1079654173767686669"', function () {
    add('843529812342341234', '236124361425345435')
      .should.equal('1079654173767686669')
  })
  it('"843579812342341234"加上"236174361425345435"等于"1079754173767686669"', function () {
    add('843579812342341234', '236174361425345435')
      .should.equal('1079754173767686669')
  })
  it('"59812342341234"加上"44361425345435"等于"104173767686669"', function () {
    add('59812342341234', '44361425345435')
      .should.equal('104173767686669')
  })
})